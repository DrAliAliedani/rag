def calculate_true_faithfulness(context, answer):
    """Evaluates each sentence of the answer against each excerpt of the context."""
    if not context.strip() or not answer.strip():
        return 0.0

    # 1. Split Answer into individual claims/sentences
    sentences = re.split(r'(?<=[.؟!\n])\s+', answer.strip())
    sentences = [s.strip() for s in sentences if len(s.strip()) > 10]

    # 2. Split Context into individual excerpts using our fences!
    # This guarantees the NLI model reads EVERY piece of the text without hitting limits.
    excerpts = context.split('--- [مقتطف')
    excerpts = [e.strip() for e in excerpts if len(e.strip()) > 20]

    if not sentences or not excerpts:
        return 0.0

    total_score = 0.0
    id2label = nli_classifier.config.id2label

    print("\n--- [35m[1m[38;5;208m[35m[1m📊[35m[1m[38;5;208m[35m[1m NLI Sentence-by-Sentence Breakdown [35m[1m[38;5;208m[35m[1m---")

    # 3. The Matrix Check
    for sentence in sentences:
        best_sentence_score = 0.0

        # Check this sentence against EVERY individual excerpt
        for excerpt in excerpts:
            inputs = nli_tokenizer(
                excerpt,
                sentence,
                truncation=True,
                max_length=512,
                return_tensors="pt"
            ).to(device)

            with torch.no_grad():
                outputs = nli_classifier(**inputs)

            probs = torch.nn.functional.softmax(outputs.logits, dim=-1)[0]

            entailment_prob = 0.0
            for idx, prob in enumerate(probs):
                if "entailment" in id2label[idx].lower():
                    entailment_prob = prob.item()
                    break

            # If this excerpt proves the sentence better, keep this score
            if entailment_prob > best_sentence_score:
                best_sentence_score = entailment_prob

        # Print the best score for this specific claim to debug
        print(f"[35m[1m[38;5;208m[35m[1m[35m[1m[38;5;208m[35m[1m🔍[35m[1m[38;5;208m[35m[1m Claim: '{sentence[:60]}...' | Score: {round(best_sentence_score * 100, 1)}%")
        total_score += best_sentence_score

    # Return the average score across all sentences
    final_score = total_score / len(sentences)
    print("---------------------------------------------")
    return float(final_score)

def ask_jais(question, search_type="hybrid", alpha=0.5, category_filter="الكل", use_reranker=True):
    start_time = time.time()

    # 1. Get Context
    context = get_context(question, search_type=search_type, alpha=alpha, category_filter=category_filter, use_reranker=use_reranker)

    'print(f"\n--- [35m[1m[38;5;208m[35m[1m📄[35m[1m[38;5;208m[35m[1m Retrieved Context [35m[1m[38;5;208m[35m[1m📄[35m[1m[38;5;208m[35m[1m ---\n{context}\n-------------------------------\n")'

    # 2. Construct Prompt
    system_prompt = "أنت مساعد ذكي يختص ياجابة اسئلة الطلبة وتوضيح تفاصيل النصوص الجامعية. استخدم القوانين والضوابط التالية للإجابة على سؤال الطالب بدقة."
    user_prompt = f"السياق:\n{context}\n\nالسؤال: {question}"

    messages = [
        {"role": "system", "content": system_prompt},
        {"role": "user", "content": user_prompt},
    ]

    # 3. Apply Chat Template & Generate
    chat_text = tokenizer.apply_chat_template(messages, tokenize=False, add_generation_prompt=True)
    inputs = tokenizer(chat_text, return_tensors="pt").to(model.device)
    inputs.pop("token_type_ids", None)

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=512,
            do_sample=True,
            temperature=0.3,
            top_p=0.9,
            pad_token_id=tokenizer.eos_token_id
        )
    answer = tokenizer.decode(outputs[0][inputs['input_ids'].shape[-1]:], skip_special_tokens=True).strip()
    latency = time.time() - start_time

    # --- 5. HYBRID EVALUATION ---

    # Relevance uses E5 Embeddings (Is the answer semantically answering the question?)
    raw_relevance = calculate_cosine_similarity(question, answer, prefix1="query: ", prefix2="passage: ")

    # Faithfulness uses NLI (Is the answer logically supported by the context without hallucinating?)
    raw_faithfulness = calculate_true_faithfulness(context, answer)

    # [35m[1m[38;5;208m[35m[1m📊[35m[1m[38;5;208m[35m[1m Calculate percentages
    rel_percentage = raw_relevance * 100
    faith_percentage = raw_faithfulness * 100

    # Save to history
    evaluation_history["latency"].append(latency)
    evaluation_history["relevance"].append(raw_relevance)
    evaluation_history["faithfulness"].append(raw_faithfulness)

    # Return the independent scores!
    return answer, round(latency, 2), round(faith_percentage, 2), round(rel_percentage, 2)

